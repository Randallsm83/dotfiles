# Miscellaneous XDG-compliant tool configurations

# =================================================================================================
# Unix compatibility ‚ÄĒ set HOME env var for tools that expect it (Node.js, Yarn, Git, etc.)
# PowerShell has $HOME but doesn't set $env:HOME; most Unix-origin tools read the env var.
# =================================================================================================
if (-not $env:HOME) { $env:HOME = $env:USERPROFILE }

# =================================================================================================
# Docker ‚ÄĒ daemon runs inside Arch WSL2 (dockerd via systemd).
# Connects via Windows portproxy: 127.0.0.1:2375 ‚Üí WSL2 VM IP:2375.
# The portproxy is set up once with admin rights (gsudo netsh ...).
# If the portproxy stops working (WSL IP changed), run: yarn docker:proxy
# =================================================================================================
if (-not $env:DOCKER_HOST) { $env:DOCKER_HOST = 'tcp://127.0.0.1:2375' }
$env:DOCKER_CONFIG = "$env:XDG_CONFIG_HOME\docker"

# =================================================================================================
# GitLab CLI
# =================================================================================================
$env:GLAB_CONFIG_DIR = "$env:XDG_CONFIG_HOME\\glab-cli"

# =================================================================================================
# GnuPG
# =================================================================================================
$env:GNUPGHOME = "$env:XDG_DATA_HOME\gnupg"

# =================================================================================================
# TLDR (simplified man pages)
# =================================================================================================
$env:TLDR_CACHE_DIR = "$env:XDG_CACHE_HOME\tldr"

# =================================================================================================
# Vagrant
# =================================================================================================
$env:VAGRANT_HOME = "$env:XDG_DATA_HOME\vagrant"

# =================================================================================================
# MySQL
# =================================================================================================
$env:MYSQL_HISTFILE = "$env:XDG_STATE_HOME\mysql\history"

# =================================================================================================
# SQLite
# =================================================================================================
$env:SQLITE_HISTORY = "$env:XDG_STATE_HOME\sqlite\history"

# =================================================================================================
# Ensure state directories exist
# =================================================================================================
$stateDirs = @(
    "$env:XDG_STATE_HOME\mysql",
    "$env:XDG_STATE_HOME\sqlite"
)

foreach ($dir in $stateDirs) {
    if (-not (Test-Path $dir)) {
        New-Item -ItemType Directory -Path $dir -Force | Out-Null
    }
}

